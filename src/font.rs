/// Bitmap font for pixel framebuffer rendering.
/// 8×16 pixel glyphs for ASCII characters 0x20-0x7E (printable).
/// Each character is 16 bytes, one byte per row (8 pixels wide).

pub const CHAR_WIDTH: usize = 8;
pub const CHAR_HEIGHT: usize = 16;

/// Get the bitmap for an ASCII character (16 bytes, MSB = leftmost pixel).
pub fn glyph(ch: u8) -> &'static [u8; CHAR_HEIGHT] {
    if ch >= 0x20 && ch <= 0x7E {
        &FONT[ch as usize - 0x20]
    } else {
        &FONT[0] // space for unknown chars
    }
}

